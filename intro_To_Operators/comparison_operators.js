//It includes: < , <= , >, >=

let n1 = 2;
let n2 = 3;

const isNumGreater = n1 > n2;
const isNumGreaterOrEqualTo = n1 >= n2;
const isNumLesser = n1 < n2;
const isNumLesserOrEqual = n1 <= n2;

console.log(isNumGreater); // false
console.log(isNumGreaterOrEqualTo); // false
console.log(isNumLesser); // true
console.log(isNumLesserOrEqual); // true 