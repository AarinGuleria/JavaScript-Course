let job = "Sales Engineer";

switch (job) {
  case "Sales Engineer":
    console.log("You are a Sales Engineer");
    break;
  case "Software Engineer":
    console.log("You are a Software Engineer");
    break;
  case "Project Manager":
    console.log("You are a Project Manager");
    break;
  default:
    console.log("You are a Sales Executive");
    break;
}
